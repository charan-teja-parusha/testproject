# Basic program in GitLab

# Import necessary libraries
import math

# Define a function for calculating the square root of a number
def square_root(number):
    return math.sqrt(number)

# Define a function for calculating the area of a circle
def circle_area(radius):
    return math.pi * radius**2

# Call the square root function
result = square_root(9)
print("The square root of 9 is:", result)

# Call the circle area function
result = circle_area(5)
print("The area of a circle with radius 5 is:", result)
